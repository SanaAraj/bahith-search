def download_from_wikipedia():
    os.makedirs(DATA_PATH, exist_ok=True)
    wikipedia.set_lang('ar')

    downloaded = 0
    for topic in TOPICS:
        try:
            page = wikipedia.page(topic, auto_suggest=False)
            content = page.content
            if len(content) < 500:
                continue

            filename = topic.replace(' ', '_').replace('/', '_') + ".txt"
            filepath = os.path.join(DATA_PATH, filename)
            with open(filepath, 'w', encoding='utf-8') as f:
                f.write(f"# {page.title}\n\n{content}")

            downloaded += 1
            print(f"Downloaded: {topic}")

            if downloaded >= 35:
                break

        except Exception as e:
            print(f"Failed to download {topic}: {e}")

    return downloaded


def use_fallback_articles():
    os.makedirs(DATA_PATH, exist_ok=True)

    for name, content in FALLBACK_ARTICLES.items():
        filename = f"{name}.txt"
        filepath = os.path.join(DATA_PATH, filename)
        with open(filepath, 'w', encoding='utf-8') as f:
            f.write(content.strip())

    return len(FALLBACK_ARTICLES)


def main():
    print("Seeding Arabic data...")

    count = download_from_wikipedia()

    if count < 10:
        print(f"Wikipedia download yielded only {count} articles. Using fallback content...")
        count = use_fallback_articles()

    files = os.listdir(DATA_PATH)
    print(f"\nTotal files created: {len(files)}")
    print("Sample files:", files[:5])


if __name__ == "__main__":
    main()
